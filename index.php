    <h1>UDG Costech</h1>
    <p>Amacı: Gruptaki üyelerle birlikte sürekli sürekli eğitim ve projelere imza atmaktır.</p>
    <p>Grubun işleyiş biçimi: İlk 2 ay için grup başkanı Tunç Ahmet Bayram olacaktır.<br>
         Sonraki dönemde yeni bir başkan seçimi olacak veya sistem yeniden düzenlenip yeni bir sistem kurulacaktır.</p>
    <h1>Ana Maddeler</h1>
    <p>Madde 1. Gruptaki yönetim kararları yönetim kurulu tarafından alınacaktır.</p>
    <p>Madde 2. Grupta alınacak/alınan kararlara tüm üyelerin itiraz hakkı bulunmaktadır.</p>
    <p>Madde 3. Proje ana ekibi 10 kişiden oluşacaktır. <br>
        4 tasarımcı 4 programcı 2 proje yöneticisinden oluşacaktır. <br>
        Proje yan ekibi 5 kişiden oluşacaktır. <br>
        2 tasarımcı 2 programcı 1 proje yöneticisinden oluşacaktır. <br>
        Jam ekibi 5 kişiden oluşacaktır. <br>
        2 tasarımcı 2 programcı 1 proje yöneticisinden oluşacaktır.</p>
    <p>Madde 4. Jam grubu sadece jam sürecinde bir araya gelecektir. <br>
        Jam olmadığı zamanlarda proje grubu ile ortak çalışacaktır veya ayrıca bir proje grubu olarak çalışacaklardır.    </p>
    <p>Madde 5. Proje ekiplerinin amacı Google Play Store için oyunlar üretip bunları yayımlamaktır. <br>
        Projelerin teslim tarihleri başkan tarafından belirlenecektir. <br>
        Üyelerin neler yapacağına dair dosyalar verilecektir. <br>
        Ekipler içinse ana bir GDD dosyası oluşturulacaktır.<br>
         Bunları proje yöneticileri belirleyecektir.</p>
    <p>Madde 6. Ekiplerde çalışanlar verilen dosyalarda yazanları yaptıkları takdirde kendilerine 5 üzerinden bir puan vereceklerdir. <br>
        Bu puanlar bulundukları ekiplerin proje yöneticilerine iletilecektir. <br>
        Proje yöneticileri bu puanları da başkana yollayacakları dosyaya ekleyeceklerdir.</p>
    <p>Madde 7. Gruptaki proje kararları oylamaya sunulacaktır. <br>
        Akademisyenlerin bu kararlara müdahale etme hakkı bulunacaktır. <br>
        Yaptıkları müdahaleler yönetim kurulu tarafından onaylanacaktır.<br>
         Onaylanan kararlar direkt olarak yürürlüğe girecektir.</p>
    <p>Madde 8. Ekiplerin işleyiş durumları ve eğitimler Başkan tarafından belirlenecektir.    </p>
    <p>Madde 9. Çalışan tüm ekipler proje dahilinde veya ekip dahilinde o hafta yaptığı tüm gelişmeleri proje yöneticilerine bildirecektir. <br>
        Proje yöneticileri ise her cuma saat 20.00’da hazırlanan raporları başkanın tarafına iletecektir.</p>
    <p>Madde 10. Tüm üyeler eğitim konusundaki ilerlemeleri her cuma 20.00’da başkan tarafına iletecektir.</p>
    <p>Madde 11. Ayın geliştiricisi, ayın tasarımcısı, ayın proje yöneticisi her ayın birinde websitesinde paylaşılacaktır. <br>
        Birinci olan üyelere sürpriz ödüller verilecektir.    </p>
    <p>Madde 12. Şirketlerle anlaşmalar yaparak onlar için oyunlar geliştirip satacağız. <br>
        Elde edilen gelirin %40’u UDG kasasına eklenecektir. %10’u Costech Kasasına eklenecektir. <br>
        %50’si emeği geçen grup üyelerine eşit olarak paylaştırılıcaktır.</p>
    <p>Madde 13. UDG kasası şunlar için kullanılacaktır:<br>
        -Jam ekibinin ulaşım/yemek masrafları.<br>
        -Grupta verilen eğitimlerin masraflarına.<br>
        -Şirket gezilerinde oluşacak olan masraflara.<br>
        -Grup için alınacak olan materyallerin masraflarına.
    </p>
    <p>Madde 14. Tüm gelir gider tabloları her hafta UDG’nin websayfasında paylaşılacaktır.</p>
    <h1>Eğitim Süreci</h1>
    <p>Eğitim süreci 1 ay sürecektir. <br>
        Eğitimdeki gerekli materyaller ücretsiz sağlanacak olup başkan tarafından üyelere verilecektir. <br>
        Birebir eğitimlerde ise başkan üyeler ile birebir görüşüp onlara özel yol haritası çizecektir. <br>
        1 ayın sonunda ise projeler ekiplere verilecektir. Proje geliştirme süreci devam ederken eğitimler devam edecektir.</p>
